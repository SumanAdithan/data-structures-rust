// HashSet
// A HashSet is a data structure that can hold a collection of values. The values however must be unique
// A HashSet can contain a mix of different data types only if defined as an enum or trait object, since Rust is strongly typed
// HashSets are dynamically sized. You don't have to declare the size of a HashSet before creating it
// HashSets do not maintain an insertion order
// HashSets are iterable. They can be used with a for loop

// HashSet vs Vec
// Vec can contain duplicate values whereas HashSets cannot
// Insertion order is maintained in Vec but it is not the case with HashSets
// Searching and deleting an element in a HashSet is faster compared to Vec
